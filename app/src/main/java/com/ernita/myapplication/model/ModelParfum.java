package com.ernita.myapplication.model;

public class ModelParfum {

    String _id, kodeParfum, namaParfum, jenisParfum, Ml, hargaParfum, gambar;

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String getKodeParfum() {
        return kodeParfum;
    }

    public void setKodeParfum(String kodeParfum) {
        this.kodeParfum = kodeParfum;
    }

    public String getNamaParfum() {
        return namaParfum;
    }

    public void setNamaParfum(String namaParfum) {
        this.namaParfum = namaParfum;
    }

    public String getJenisParfum() {
        return jenisParfum;
    }

    public void setJenisParfum(String jenisParfum) {
        this.jenisParfum = jenisParfum;
    }

    public String getMl() {
        return Ml;
    }

    public void setMl(String ml) {
        Ml = ml;
    }

    public String getHargaParfum() {
        return hargaParfum;
    }

    public void setHargaParfum(String hargaParfum) {
        this.hargaParfum = hargaParfum;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }
}
